    private void rbBancoRoupaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbBancoRoupaActionPerformed
       this.dispose();
        TelaMenuDonatarioRecebeRoupa telaRecebeRoupa = new TelaMenuDonatarioRecebeRoupa();
        TelaPrincipal.painel.add(telaRecebeRoupa);
        telaRecebeRoupa.setVisible(true);
        
        String recebidas = Integer.toString(DonatarioControl.verRoupasRecebidas(TelaLoginDonatario.txtLogDonaLogin.getText(),TelaLoginDonatario.txtLogDonaSenha.getText()));
        TelaMenuDonatarioRecebeRoupa.txtRecebidas.setText(recebidas);
        
         String limite = Integer.toString(DonatarioControl.limiteRoupas());
        txtLimitePecas.setText(limite);
        
        DonatarioControl.salvarBancoRoupas();
        for(int i=0;i<Pessoa.bancoRoupas2.size();i++){
                TelaMenuDonatarioRecebeRoupa.tbRoupasReceber.setValueAt(Pessoa.bancoRoupas2.get(i), i, 1);
            }
    }//GEN-LAST:event_rbBancoRoupaActionPerformed

    private void rbVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbVisualizarActionPerformed
        this.dispose();
        TelaMenuDonatarioVisualizar telaVisualizar = new TelaMenuDonatarioVisualizar();
        TelaPrincipal.painel.add(telaVisualizar);
        telaVisualizar.setVisible(true);
        DonatarioControl.salvarBancoRoupas();
        DonatarioControl.salvarBancoDinheiro();
        for(int i=0;i<Pessoa.bancoRoupas2.size();i++){
                TelaMenuDonatarioVisualizar.tbRoupasVisu.setValueAt(Pessoa.bancoRoupas2.get(i), i, 1);
            }
         String dinheiroDoado = Double.toString(Pessoa.dinheiroDoado);
        TelaMenuDonatarioVisualizar.txtQtdeDin.setText(dinheiroDoado);
    }//GEN-LAST:event_rbVisualizarActionPerformed

